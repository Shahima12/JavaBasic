package com.syntax.class03;

public class ItElseIf {

	public static void main(String[] args) {
		
		int day=1;
		if(day==1) {
			
			System.out.println("Today is monday");
			
		} else if (day==2) {
			
			System.out.println("Today is Tuesday");
			
		}else if (day==3) {
			System.out.println("Today is Wednesday");
		}
	}

}
